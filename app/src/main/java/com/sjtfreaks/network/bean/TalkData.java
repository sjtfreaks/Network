package com.sjtfreaks.network.bean;

public class TalkData {
    private String title;
    private String im_ava;
    private String url;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getIm_ava() {
        return im_ava;
    }

    public void setIm_ava(String im_ava) {
        this.im_ava = im_ava;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
